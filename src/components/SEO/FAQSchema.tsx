import React from 'react'

export interface FAQItem {
  question: string
  answer: string
  category?: string
  keywords?: string[]
}

export interface FAQSchemaProps {
  faqs: FAQItem[]
  title?: string
  description?: string
}

/**
 * FAQ Schema компонент для структурированных данных
 * Генерирует JSON-LD для FAQ страниц и секций
 */
export default function FAQSchema({ faqs, title, description }: FAQSchemaProps) {
  if (!faqs || faqs.length === 0) return null

  const faqData = {
    '@context': 'https://schema.org',
    '@type': 'FAQPage',
    ...(title && { name: title }),
    ...(description && { description }),
    mainEntity: faqs.map((faq, index) => ({
      '@type': 'Question',
      name: faq.question,
      acceptedAnswer: {
        '@type': 'Answer',
        text: faq.answer,
      },
      ...(faq.keywords && {
        keywords: faq.keywords.join(', '),
      }),
      position: index + 1,
    })),
  }

  return (
    <script
      type="application/ld+json"
      dangerouslySetInnerHTML={{
        __html: JSON.stringify(faqData),
      }}
    />
  )
}

